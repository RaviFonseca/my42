#include <unistd.h>
#include <stdlib.h>
#include "libft.h"

void	*ft_memalloc(size_t size)
{
	void	*ptr;

	ptr = malloc(size);
	if (!ptr)
		return (NULL);
	ft_bzero(ptr, size);
	return (ptr);
}

#include <stdio.h>

int main(void)
{
	char *ptr;
	ptr = ft_memalloc(5);
	printf("os valores de ptr: %s\n", ptr);
	return (0);
}
